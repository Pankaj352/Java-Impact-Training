

public class AssignmentOperator {
    public static void main(String[] args) {
        int x=10,y;

//        =
        y=x;
        System.out.println(y);//10
//        +=
        x+=y;
        System.out.println(x); //20

//        -=
        x-=y;
        System.out.println(x);//10


//        /=

         x/=y;
        System.out.println(x);//1

//        *=

        x*=y;
        System.out.println(x);//10


    }
}

public class swapping {
    public static void main(String[] args) {
        int x=10;
        int y=20;
        int z;
        System.out.println("Before swapping the value of x and y :"+x+" "+y);
        z=x;
        x=y;
        y=z;
        System.out.println("After swapping the value of x and y :"+x+" "+y);


    }
}

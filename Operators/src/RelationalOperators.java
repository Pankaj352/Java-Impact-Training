import java.util.Scanner;

public class RelationalOperators {
    public static void main(String[] args) {
        int p=20,q=30;
        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p>q);
        System.out.println(p<q);
        System.out.println(p>=q);
        System.out.println(p<=q);


    }
}
